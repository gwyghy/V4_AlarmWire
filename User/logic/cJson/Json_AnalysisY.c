/*******************************************************************************************
**函数名称：getComAPI
**函数作用：获取参数公共函数
**函数参数：无
**函数输出：无
**注意事项：无
*******************************************************************************************/
cJSON* getComAPI(uint64_t levels)
{
	uint8_t i = 0,j = 0,leveltmp = 0;
	cJSON *tmp = v4Json;
	if(levels == 0)
		return NULL;
	
	for(j = 0; j < 5; j++)
	{
		//计算目录
		leveltmp = ((levels>>((7-j)*8))&0xff);
		if(leveltmp == 0)
			break;
		if(!tmp->child)
			return NULL;
		
		tmp = tmp->child;
		if(tmp->type == cJSON_Array)//跳过array的cjson结构体
		{
			if(!tmp->child) 
				return NULL;
			tmp = tmp->child;
		}
		//遍历对应次数
		for(i = 1; i<leveltmp; i++)
		{
			if(!tmp->next)
				return NULL;
			tmp = tmp->next;
		}
	}
	return tmp;
}


