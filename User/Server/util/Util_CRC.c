static u8	s_u8CRC_TABLE[]= {0x00,0x31,0x62,0x53,0xC4,0xF5,0xA6,0x97,0xB9,0x88,0xDB,0xEA,0x7D,0x4C,0x1F,0x2E};	 //CRC校验表

/***********************************************************
** 函数名：CRC_8
** 输　入：unsigned char *PData,unsigned char Len
** 输　出：CRC校验结果
** 功能描述：8位CRC校验函数
************************************************************/
uint8_t	CRC_8(uint8_t *PData, uint8_t Len)
{
	u8	CRC_Temp=0;
	u8	Temp, i;
	u8	PData_H=0;
	u8	PData_L=0;

	for (i = 0; i < Len; i++)
	{
		PData_L=PData[i];
		if (i < (Len-1))
		{
			PData_H=PData[i+1];

			Temp=CRC_Temp>>4;
			CRC_Temp<<=4;
			CRC_Temp^=s_u8CRC_TABLE[Temp^(PData_H>>4)];
			Temp=CRC_Temp>>4;
			CRC_Temp<<=4;
			CRC_Temp^=s_u8CRC_TABLE[Temp^(PData_H&0x0F)];
			i++;
		}

		Temp=CRC_Temp>>4;
		CRC_Temp<<=4;
		CRC_Temp^=s_u8CRC_TABLE[Temp^(PData_L>>4)];
		Temp=CRC_Temp>>4;
		CRC_Temp<<=4;
		CRC_Temp^=s_u8CRC_TABLE[Temp^(PData_L&0x0F)];
	}

	return (CRC_Temp);
}


/*******************************************************************************************
**函数名称：vCrc16Ccitt
**函数作用：计算CRC16校验值
**函数参数：u8Buf:需要校验的数据起始地址，u32Len:需要校验数据的长度，*u16CheckOld:计算后的CRC校验值
**函数输出：无
**注意事项：无
*******************************************************************************************/
void Crc16Ccitt(const u8 *u8Buf, u32 u32Len, u16 *u16CheckOld)
{
	u32 u32Cnt = 0x00;
	u16 u16Crc = *u16CheckOld;
		#if OS_CRITICAL_METHOD == 3                   
		OS_CPU_SR  cpu_sr = 0;
	#endif
	
	OS_ENTER_CRITICAL();
	for( u32Cnt = 0; u32Cnt < u32Len; u32Cnt++)
		u16Crc = (u16Crc<<8) ^ u16Crc16tab[((u16Crc>>8) ^ *(u8 *)u8Buf++)&0x00FF];
	*u16CheckOld = u16Crc;
	OS_EXIT_CRITICAL();
//	*u16CheckOld = 0;
}

